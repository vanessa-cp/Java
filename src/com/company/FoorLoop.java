package com.company;

public class FoorLoop {
    public static void main(String[] args) {

        for (int i = 0; i <= 10; i++) { //int y declaramos una variable
            //condicion de comienzo, condicion de parada, condicion de paso(como se va a ir incrementando ese bucle)

            System.out.println(i + " Hola mundo");
        }
        System.out.println("fin");
    }
}
